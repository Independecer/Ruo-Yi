public enum PayType {


    wallet(1,"钱包支付"),

    wechat(2,"微信支付");
    private final int value;
    private final String remark;

    PayType(int value, String remark) {
        this.value = value;
        this.remark = remark;
    }

    public int getValue() {
        return value;
    }

    public String getRemark() {
        return remark;
    }
}
